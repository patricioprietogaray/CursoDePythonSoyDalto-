import math

def sumar(a,b):
    return a+b

def restar(a,b):
    return a-b

def multiplicar(a,b):
    return a*b

def dividir(a,b):
    return a/b

def modulo(a,b):
    return a%b

def radicacion(a):
    math.sqrt(a)

def hypotenusa(a,b):
    return math.hypot(a,b)

def factorial(a):
    return math.factorial(a)

numero_pi = math.pi
numero_e = math.e