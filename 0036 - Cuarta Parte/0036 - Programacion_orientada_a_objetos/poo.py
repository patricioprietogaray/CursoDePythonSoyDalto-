# Definición de la programación orientada a objetos:
# La POO es un paradigma de programación que utiliza objetos 
# y que representa cosas o conceptos del mundo real 
# y sus interacciones para modelar y resolver problemas de la vida real. 
# Los objetos son instancias de clases, que representan entidades con 
# propiedades (atributos) y acciones (métodos) específicas. 
# La POO se centra en el encapsulamiento, la herencia 
# y el polimorfismo que facilitan la reutilizacion del código, 
# la abstracción y la modularidad.

